package com.intel.daal.algorithms.neural_networks.prediction;

/**
 * <a name="DAAL-CLASS-ALGORITHMS__NEURAL_NETWORKS__PREDICTION__PREDICTIONMODELINPUTID"></a>
 * \brief Available identifiers of input Model objects in the prediction stage
 *        of the Neural Networks algorithm
 */
public final class PredictionModelInputId {
    /** @private */
    static {
        System.loadLibrary("JavaAPI");
    }

    private int _value;

    /**
     * Constructs the input object identifier using the provided value
     * @param value     Value of the input object identifier
     */
    public PredictionModelInputId(int value) {
        _value = value;
    }

    /**
     * Returns the value corresponding to the input object identifier
     * @return Value corresponding to the input object identifier
     */
    public int getValue() {
        return _value;
    }

    private static final int modelId = 1;

    public static final PredictionModelInputId model = new PredictionModelInputId(modelId);
        /*!< Input model trained by the Neural Networks algorithm */
}
